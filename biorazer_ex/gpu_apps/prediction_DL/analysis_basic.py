@dataclass
class SinglePrediction(SequenceEntry):
    """
    A class to represent a single prediction generated by common deep learning models.
    - A single prediction may contains multiple seeds and multiple samples.
    - Structures and confidences are stored in self.my_dir.
    """

    def is_formatted(self):
        """
        Check if the prediction is formatted.
        - A formatted prediction should have a directory structure like this:
        ```
        my_dir/
            seed-1_sample-1/
                my_dir_seed-1_sample-1_model.cif
                my_dir_seed-1_sample-1_model.pdb
                my_dir_seed-1_sample-1_data.pickle
            seed-1_sample-2/
                ...
            seed-2_sample-1/
                ...
            ...
        ```
        """
        entry_dir_path = self.get_dir_path()
        samples = list(entry_dir_path.glob("seed-*_sample-*"))
        if len(samples) == 0:
            return False
        for sample in samples:
            if not sample.is_dir():
                return False
            cif_model = sample / (
                entry_dir_path.stem + "_" + sample.stem + "_model.cif"
            )
            pdb_model = sample / (
                entry_dir_path.stem + "_" + sample.stem + "_model.pdb"
            )
            meta_data = sample / (
                entry_dir_path.stem + "_" + sample.stem + "_data.pickle"
            )
            if (
                not cif_model.exists()
                or not pdb_model.exists()
                or not meta_data.exists()
            ):
                return False
        return True

    def collect_samples(self, overwrite=False):
        """
        Collect all samples in the prediction directory.
        - Returns a DataFrame with columns: seed, sample, cif_model, pdb_model, meta_data
        - The returned dataframe is also attached to the SinglePrediction object as self.samples
        """
        entry_dir_path = self.get_dir_path()
        if not self.is_formatted():
            raise ValueError(
                f"{entry_dir_path} is not formatted. Please call format_output() first."
            )

        samples = list(entry_dir_path.glob("seed-*_sample-*"))
        all_data = []
        for i, predicted_sample in enumerate(samples):
            seed, sample = re.match(
                r"seed-(\d+)_sample-(\d+)", predicted_sample.stem
            ).groups()
            cif = str(
                predicted_sample
                / (entry_dir_path.stem + "_" + predicted_sample.stem + "_model.cif")
            )
            pdb = str(
                predicted_sample
                / (entry_dir_path.stem + "_" + predicted_sample.stem + "_model.pdb")
            )
            pickle = str(
                predicted_sample
                / (entry_dir_path.stem + "_" + predicted_sample.stem + "_data.pickle")
            )
            all_data.append(
                dict(
                    seed=int(seed), sample=int(sample), cif=cif, pdb=pdb, pickle=pickle
                )
            )
        self.samples = pd.DataFrame(all_data)
        self.samples.sort_values(by=["seed", "sample"], inplace=True, ignore_index=True)

    def get_basic_info(self):
        self.sequence = PDB2SEQ(self.get_samples().iloc[0]["pdb"]).read()

    def flatten_samples(self):
        results = pd.DataFrame()
        results.loc[0, "Marker"] = self.marker
        properties = [
            i for i in self.get_samples().columns if not i in ["seed", "sample"]
        ]

        for property in properties:
            for i in self.get_samples().index:
                seed = self.get_samples().loc[i, "seed"]
                sample = self.get_samples().loc[i, "sample"]
                results.loc[0, f"{property}_seed-{seed}_sample-{sample}"] = (
                    self.get_samples().loc[i, property]
                )

        return results

    def extract_simple_metrics(
        self,
        metric_key_list: list[str],
        metric_id_list: list[tuple],
        metric_label_list: list[str],
    ):
        """
        Extracts confidence data from the prediction results.

        Parameters
        ----------
        metric_key_list : list[str]
            List of keys to extract from the confidence data.
        metric_id_list : list[tuple]
            List of tuples, where each tuple contains indices to access the nested data structure.
            - For example, [0, 1] extracts metadata[key][0][1] as the metric
        metric_label_list : list[str]
            List of labels corresponding to the metrics.

        Return
        -------
        samples : pd.DataFrame
            DataFrame containing the extracted metrics for each sample.
            The columns will be named according to the metric_label_list.
            Each row corresponds to a sample, and the columns contain the extracted metric values.
        """
        if not hasattr(self, "samples"):
            raise ValueError(
                "Samples are not collected. Please call collect_samples() first."
            )

        samples = self.get_samples()
        for sample_i in samples.index:

            seed, sample = samples.loc[sample_i, ["seed", "sample"]]
            metadata = self.get_metadata(seed, sample, metric_key_list)
            for metric_key, metric_id, metric_label in zip(
                metric_key_list, metric_id_list, metric_label_list
            ):
                metric_data = metadata[metric_key]
                if not isinstance(metric_data, list):
                    data = metric_data
                else:
                    data = None
                    for j in metric_id:
                        if data is None:
                            data = metric_data[j]
                        else:
                            data = data[j]
                samples.loc[sample_i, metric_label] = data
        return samples

    def get_metadata_keys(self):
        """
        Get the keys of the meta data.
        """
        tmp_meta_data = pickle.load(open(self.samples.loc[0, "pickle"], "rb"))
        return list(tmp_meta_data.keys())

    def get_metadata(self, seed, sample, metadata_keys):
        """
        Get the meta data of a specific sample.
        seed: seed of the sample
        sample: sample number
        meta_data_key: key of the meta data
        """
        selector = (self.samples["seed"] == seed) & (self.samples["sample"] == sample)
        selected_sample = self.samples[selector]
        metadata = pickle.load(open(selected_sample.iloc[0]["pickle"], "rb"))
        res = dict()
        for key in metadata_keys:
            if key not in metadata:
                raise ValueError(
                    f"{key} not found in {selected_sample.iloc[0]['pickle']}"
                )
            res[key] = metadata[key]
        return res

    def get_structure(self, seed, sample, file_type="cif"):
        """
        Get the structure of a specific sample.
        seed: seed of the sample
        sample: sample number
        file_type: type of the file to return, can be "cif" or "pdb"
        """
        selector = (self.samples["seed"] == seed) & (self.samples["sample"] == sample)
        selected_sample = self.samples[selector]
        if file_type == "cif":
            return CIF2STRUCT(selected_sample.iloc[0]["cif"], "").read()
        elif file_type == "pdb":
            return PDB2STRUCT(selected_sample.iloc[0]["pdb"], "").read()
        else:
            raise ValueError(f"Unsupported file type: {file_type}")

    def get_sample(self, seed, sample):
        """
        Get a specific sample by seed and sample number.
        Returns a DataFrame with the sample data.
        """
        selector = (self.samples["seed"] == seed) & (self.samples["sample"] == sample)
        return self.samples[selector].reset_index(drop=True)

    def report_atom_plddts(self, seed, sample):
        samples = self.samples
        selector = (samples["seed"] == seed) & (samples["sample"] == sample)
        metadata = pickle.load(open(samples.loc[selector, "pickle"].values[0], "rb"))
        atom_plddts = metadata["atom_plddts"]
        cif = samples.loc[selector, "cif"].values[0]
        cif_plddt = str(Path(cif).with_stem(Path(cif).stem + "_plddt"))
        parser = CIF2CIF(cif, cif_plddt)
        structure = parser.read()
        structure.set_annotation(
            "b_factor", atom_plddts
        )  # 不能使用 structure.b_factor = atom_plddt
        parser.write(structure)

    def report_residue_plddts(self, seed, sample):
        pass

    def report_pae(
        self, seed, sample, figsize=(5, 5), vmin=0, vmax=31.75, cmap="coolwarm"
    ):
        sample = self.get_sample(seed, sample)
        metadata = pickle.load(open(sample.iloc[0]["pickle"], "rb"))
        pae = metadata["pae"]
        fig, ax = plt.subplots(1, 1, figsize=figsize)
        mappable = ax.imshow(pae, cmap="coolwarm", vmin=vmin, vmax=vmax)
        cbar = fig.colorbar(mappable=mappable)
        cbar.set_label("PAE (Å)")
        return fig, ax

    def calc_ipsae(
        self, chain1, chain2, pae_cutoff=10.0, dist_cutoff=10.0, pair_type="protein"
    ):
        from .advanced_metrics.ipsae import calc_ipsae

        self.samples["ipSAE"] = 0.0
        for sample_i in self.samples.index:
            seed, sample = self.samples.loc[sample_i, ["seed", "sample"]]
            pae = self.get_metadata(seed, sample, ["pae"])["pae"]
            structure = self.get_structure(seed, sample, "cif")
            ipsae = calc_ipsae(
                pae,
                structure,
                chain1,
                chain2,
                pae_cutoff=pae_cutoff,
                dist_cutoff=dist_cutoff,
                pair_type=pair_type,
            )
            self.samples.loc[sample_i, "ipSAE"] = ipsae


class BatchPrediction(SequenceLibrary):
    """
    A class to represent a batch of predictions generated by common deep learning models.
    """

    entry_type = SinglePrediction

    @staticmethod
    def _format_output(kwargs):
        single_pred: SinglePrediction = kwargs["single_pred"]
        target_dir = kwargs["target_dir"]
        return single_pred.format_output(target_dir)

    def format_output(self, target_dir, processes=1):
        """
        This function formats all single predictions in self.my_dir and saves them to target_dir.
        - Formatting is done by calling the format_output method of each single prediction.

        """

        target_dir = Path(target_dir)
        if not target_dir.exists():
            target_dir.mkdir(parents=True)

        formatted_predictions = []
        kwargs_list = [
            {"single_pred": pred, "target_dir": target_dir} for pred in self.entries
        ]

        with multiprocessing.Pool(processes=processes) as pool:
            for _, formatted_single_predction in tqdm(
                pool.imap_unordered(
                    self._format_output,
                    kwargs_list,
                ),
                total=len(self.entries),
                desc="Formatting predictions",
            ):
                formatted_predictions.append(formatted_single_predction)

        return type(self)(target_dir, formatted_predictions)

    @staticmethod
    def _extract_simple_metrics(
        kwargs,
    ):
        i = kwargs["i"]
        single_pred: SinglePrediction = kwargs["single_pred"]
        metric_key_list = kwargs["metric_key_list"]
        metric_id_list = kwargs["metric_id_list"]
        metric_label_list = kwargs["metric_label_list"]
        return i, single_pred.extract_simple_metrics(
            metric_key_list, metric_id_list, metric_label_list
        )

    def extract_simple_metrics(
        self,
        metric_key_list: list[str],
        metric_id_list: list[tuple],
        metric_label_list: list[str],
        processes=1,
    ):
        """
        This function extracts metrics from all single predictions in the batch.

        Parameters
        ----------
        metric_key_list : list[str]
            List of keys to extract from the confidence data.
        metric_id_list : list[tuple]
            List of tuples, where each tuple contains indices to access the nested data structure.
            For example, [0, 1] extracts metadata[key][0][1] as the metric
        metric_label_list : list[str]
            List of labels corresponding to the metrics.

        Return
        -------
        samples : pd.DataFrame
            DataFrame containing the extracted metrics for each sample.
            The columns will be named according to the metric_label_list.
            Each row corresponds to a sample, and the columns contain the extracted metric values.
            At the same time, the dataframe is also attached to the BatchPrediction object as self.samples.
        """

        kwargs_list = [
            dict(
                i=i,
                single_pred=self.entries[i],
                metric_key_list=metric_key_list,
                metric_id_list=metric_id_list,
                metric_label_list=metric_label_list,
            )
            for i in range(len(self.entries))
        ]

        with multiprocessing.Pool(processes=processes) as pool:
            for i, samples in tqdm(
                pool.imap_unordered(
                    self._extract_simple_metrics,
                    kwargs_list,
                ),
                total=len(self.entries),
                desc="Extracting metrics",
            ):
                self[i].samples = samples

    @staticmethod
    def rerank_models(
        af3_res: pd.DataFrame, model_num: int, rerank_by: str, ascending: bool = False
    ):
        """
        使用 merge_multiple_af3_res 函数合并多个 AF3 预测后, 你可以使用这个函数对所有的 model 重新进行排序.
        这个函数会在 dataframe 中添加新的列 i_map, 例如 0_map = 1, 1_map =2, 2_map = 3, ...
        它表示现在的 model 0 对应原来的 model 1, model 1 对应 model 2, ...
        - ascending: 是否升序排列 (default: False)
        """
        base_cols = set()
        for col in af3_res.columns:
            matched = re.match(r"(\d+)_(.*)", col)
            if matched:
                base_cols.add(matched.group(2))
        if not rerank_by in base_cols:
            raise ValueError(
                f"rerank_by {rerank_by} not found in columns. Please use one of {base_cols}"
            )

        for row_i in af3_res.index:
            cols_for_reranking = [f"{i+1}_{rerank_by}" for i in range(model_num)]
            new_order = np.argsort(af3_res.loc[row_i, cols_for_reranking].values)
            if not ascending:
                new_order = new_order[::-1]
            for base_col in base_cols:
                related_cols = [f"{i+1}_{base_col}" for i in range(model_num)]
                af3_res.loc[row_i, related_cols] = af3_res.loc[
                    row_i, related_cols
                ].values[new_order]
            af3_res.loc[row_i, [f"{i+1}_map" for i in range(model_num)]] = new_order

    @staticmethod
    def plot_rank_differences(
        data_list: list[pd.DataFrame],
        marker_col: str,
        markers: list[str],
        labels: list[str],
        ymin: int,
        ymax: int,
        ylabel="Rank",
        figsize=(10, 10),
        show_diff=False,
        colors=[],
        col_to_show_number="Average_i_pTM",
        col_to_show_number_short="ipTM",
        legend_cols=1,
    ):
        plt.figure(figsize=figsize)
        df_num = len(data_list)
        if len(colors) == 0:
            colors = [f"C{i}" for i in range(len(markers))]
        elif len(colors) != len(markers):
            raise ValueError(
                "The number of colors should be the same as the number of designs."
            )
        else:
            pass

        for i, marker in enumerate(markers):
            if not show_diff:
                label = (
                    marker
                    + f": {col_to_show_number_short} "
                    + " vs. ".join(
                        [
                            f"{data.loc[data[marker_col] == marker, col_to_show_number].values[0]:.2f}"
                            for data in data_list
                        ]
                    )
                )
                plt.plot(
                    range(df_num),
                    [
                        data.loc[data[marker_col] == marker, :].index[0] + 1
                        for data in data_list
                    ],
                    "o-",
                    label=label,
                    color=colors[i],
                )
            else:
                if df_num != 2:
                    raise ValueError("Only support two dataframes for now.")
                before = (
                    data_list[0].loc[data_list[0][marker_col] == marker, :].index[0] + 1
                )
                after = (
                    data_list[1].loc[data_list[1][marker_col] == marker, :].index[0] + 1
                )
                label = (
                    marker
                    + f": {col_to_show_number_short} "
                    + " vs. ".join(
                        [
                            f"{data.loc[data[marker_col] == marker, col_to_show_number].values[0]:.2f}"
                            for data in data_list[0:2]
                        ]
                    )
                )
                if after - before < 0:
                    plt.plot([0, 1], [before, after], "o-", label=label, color="red")
                else:
                    plt.plot([0, 1], [before, after], "o-", label=label, color="green")

        plt.ylim(ymax, 0)
        # 隐藏 x 轴
        plt.xticks(range(df_num), labels)
        # plt.yticks(np.arange(0, ymax+1, 1))
        plt.yticks([])
        plt.ylabel(ylabel)
        plt.legend(
            bbox_to_anchor=(1.05, 1),
            loc="upper left",
            borderaxespad=0.0,
            ncol=legend_cols,
        )

    @staticmethod
    def plot_metric_differences(
        data_list: list[pd.DataFrame],
        marker_col: str,
        markers: list[str],
        labels: list[str],
        ymin: float,
        ymax: float,
        metric_name: str,
        ylabel="Metric Name",
        show_diff=False,
        colors=[],
        figsize=(10, 10),
        metric_name_short=None,
        step=0.025,
        legend_bbox_to_anchor=(1.05, 1),
        legend_cols=1,
    ):
        fig, ax = plt.subplots(1, 1, figsize=figsize)
        df_num = len(data_list)
        if not metric_name_short:
            metric_name_short = metric_name
        if len(colors) == 0:
            colors = [f"C{i}" for i in range(len(markers))]
        elif len(colors) != len(markers):
            raise ValueError(
                "The number of colors should be the same as the number of designs."
            )
        else:
            pass
        for i, design in enumerate(markers):
            if not show_diff:
                label = (
                    design
                    + f": {metric_name_short} "
                    + " vs. ".join(
                        [
                            f"{data.loc[data[marker_col] == design, metric_name].values[0]:.2f}"
                            for data in data_list
                        ]
                    )
                )
                ax.plot(
                    range(df_num),
                    [
                        data.loc[data[marker_col] == design, metric_name].values[0]
                        for data in data_list
                    ],
                    "o-",
                    label=label,
                    color=colors[i],
                )
            else:
                if df_num != 2:
                    raise ValueError("Only support two dataframes for now.")
                before = (
                    data_list[0]
                    .loc[data_list[0][marker_col] == design, metric_name]
                    .values[0]
                )
                after = (
                    data_list[1]
                    .loc[data_list[1][marker_col] == design, metric_name]
                    .values[0]
                )
                label = (
                    design
                    + f": {metric_name_short} "
                    + " vs. ".join(
                        [
                            f"{data.loc[data[marker_col] == design, metric_name].values[0]:.2f}"
                            for data in data_list[0:2]
                        ]
                    )
                )
                if after - before < 0:
                    plt.plot([0, 1], [before, after], "o-", label=label, color="red")
                else:
                    plt.plot([0, 1], [before, after], "o-", label=label, color="green")

        ax.set_ylim(ymin, ymax)
        # 隐藏 x 轴
        ax.set_xticks(range(df_num), labels)
        ax.set_yticks(np.arange(ymin, ymax + 0.001 * step / abs(step), step))
        ax.set_ylabel(ylabel)
        ax.legend(
            bbox_to_anchor=legend_bbox_to_anchor,
            loc="upper left",
            borderaxespad=0.0,
            ncol=legend_cols,
        )
        return fig, ax

    @staticmethod
    def format_metric_distribution_plot(
        pred_stat: pd.DataFrame,
        ax: plt.Axes,
        xlabel: str,
        ylabel: str,
        title: str,
        legend_loc: str = "upper left",
        marker_col: str = "Marker",
    ):
        ax.set_yticks(range(pred_stat.shape[0]))
        ax.set_yticklabels(pred_stat[marker_col])
        ax.set_ylim(pred_stat.shape[0], -1)
        ax.set_xlabel(xlabel)
        ax.set_ylabel(ylabel)
        ax.legend(loc=legend_loc)
